print(10 % 2)  # % (modulo) returns the remainder of the division.
print(10 / 2)  # '/' (division) returns the quotient as a floating-point number.
print(10 // 2)  # '//' (floor division) returns the quotient as an integer, rounded down.

# any one number with decimal will return float
# if you divide 2 whole numbers it will return float
print(2.0 + 5)  # type float
print(2 + 5)  # type int
print((2 + 3) * 2)  # type int
